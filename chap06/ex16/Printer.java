package ex16;

public class Printer {

	void println(int something) {
		System.out.println(something);
	}

	void println(boolean something) {
		System.out.println(something);
	}


	void println(double something) {
		System.out.println(something);
	}


	void println(String something) {
		System.out.println(something);
	}

}
