package ex17;

public class Printer {

	public static void println(int something) {
		System.out.println(something);
	}
	public static void println(boolean something) {
		System.out.println(something);
	}
	public static void println(double something) {
		System.out.println(something);
	}
	public static void println(String something) {
		System.out.println(something);
	}
}
