package example;

public class IfExample {
	public static void main(String[] args) {
		int score = 93;
		
		if (score >= 90) {
			System.out.println("more than 90");
			System.out.println("grade A");
		}
		
		if (score < 90) {
			System.out.println("less than 90");
			System.out.println("grade B");
		}
		
	}
}
