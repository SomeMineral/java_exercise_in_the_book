package example;

public class ForPrintFrom1To10Example {
	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		
		System.out.println();
		int j = 1;
		for (; j <= 3; j++) {
			System.out.println(j);
		}
		System.out.println();
		System.out.println(j);
		}
	}
}
